// Build-time font subsetting. fontkit's encoder is reliable in Node (it is NOT
// in the Workers runtime), so we precompute the subsets here and commit the
// result as src/font-subsets.ts. Re-run with `npm run build:fonts` if the font
// or charsets change.
import * as fontkit from "fontkit";
import { readFileSync, writeFileSync } from "fs";

const NUMERIC_CHARS = "0123456789 x"; // default "W x H" dimension text
const font = fontkit.create(readFileSync("AauxOffice-Bold.ttf"));

function subsetBase64(chars) {
  const s = font.createSubset();
  const seen = new Set();
  for (const g of font.glyphsForString(chars)) {
    if (!seen.has(g.id)) { seen.add(g.id); s.includeGlyph(g.id); }
  }
  const enc = s.encode();
  const u8 = enc instanceof Uint8Array ? enc : new Uint8Array(enc);
  return Buffer.from(u8).toString("base64");
}

const numeric = subsetBase64(NUMERIC_CHARS);
const ascii = subsetBase64(
  Array.from({ length: 95 }, (_, i) => String.fromCharCode(32 + i)).join(""),
);

writeFileSync(
  "src/font-subsets.ts",
  `// AUTO-GENERATED by scripts/build-font-subsets.mjs — do not edit by hand.
// Base64 TrueType subsets of AauxOffice-Bold for embedding in generated SVGs.
export const NUMERIC_CHARS = ${JSON.stringify(NUMERIC_CHARS)};
export const SUBSET_NUMERIC = ${JSON.stringify(numeric)};
export const SUBSET_ASCII = ${JSON.stringify(ascii)};
`,
);
console.log(`wrote src/font-subsets.ts (numeric=${numeric.length} ascii=${ascii.length} b64 chars)`);
